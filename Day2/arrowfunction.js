function square(x) {
    return x*x;
}

console.log('Square of 2: ' + square(2));

var square = function (x) {
    return x*x;
}

console.log('Square of 3: ' + square(3));

// Arrow function. Anonymous inner function?
var squarefn = (x) => {
    return x*x;
};

console.log('Square of 4: ' + squarefn(4));










